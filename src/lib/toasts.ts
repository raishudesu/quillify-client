import { toast } from "react-toastify";

export const loginSuccessToast = () => {
  toast.success("Logged in successfully", {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
};

